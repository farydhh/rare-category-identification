"""
Background Splitting Dataset Mapper
======================================
Custom Detectron2 dataset mapper that loads background pseudo-labels
alongside the standard COCO annotations.

For each training image, this mapper:
1. Applies standard Detectron2 data augmentation (resize, flip, etc.)
2. Loads the background pseudo-label generated by the Teacher model.
3. Attaches the pseudo-label to the dataset dict so the auxiliary
   loss in BackgroundSplittingROIHeads can use it.

Supports both string labels (from DeepLabV3: {"image_key": "indoor"})
and integer labels (from Places365: {"image_key": 3}).
"""

import json
import copy
import os
import torch
import numpy as np
from detectron2.data import detection_utils as utils
from detectron2.data import transforms as T


class BackgroundSplitDatasetMapper:
    """Maps dataset dicts to training format with background pseudo-labels.

    This mapper replaces Detectron2's default DatasetMapper during
    Background Splitting training. It augments each training sample
    with a 'bg_label' field containing the integer pseudo-label
    for that image's dominant background scene type.

    Args:
        cfg: Detectron2 config.
        is_train: Whether in training mode.
        augmentations: List of augmentation transforms.
        image_format: Image channel format ("BGR" or "RGB").
        recompute_boxes: Whether to recompute boxes from masks.
        bg_labels_file: Path to JSON file with pseudo-labels.
    """

    BG_CATEGORIES = {
        "generic_background": 0,
        "indoor": 1,
        "nature_outdoor": 2,
        "road_urban": 3,
        "water": 4,
        "sky_aerial": 5,
        "person_context": 6,
    }

    def __init__(self, cfg, is_train=True, augmentations=None,
                 image_format="BGR", recompute_boxes=False,
                 bg_labels_file=None):
        self.is_train = is_train
        self.augmentations = augmentations or utils.build_augmentation(
            cfg, is_train
        )
        self.image_format = image_format
        self.recompute_boxes = recompute_boxes

        # Load background pseudo-labels from JSON
        self.bg_labels = {}
        if bg_labels_file:
            with open(bg_labels_file, "r") as f:
                raw = json.load(f)
            for k, v in raw.items():
                if isinstance(v, dict):
                    # DeepLabV3 format: {"bg_class": "indoor"}
                    self.bg_labels[k] = self.BG_CATEGORIES.get(
                        v.get("bg_class", "generic_background"), 0
                    )
                elif isinstance(v, int):
                    # Places365 format: integer label directly
                    self.bg_labels[k] = v
                else:
                    # String label format
                    self.bg_labels[k] = self.BG_CATEGORIES.get(v, 0)
            print(
                f"[BGSplitMapper] Loaded {len(self.bg_labels)} "
                f"background labels from {bg_labels_file}"
            )

    def __call__(self, dataset_dict):
        """Process a single dataset entry.

        Returns the standard Detectron2 format plus a 'bg_label'
        field containing the integer pseudo-label for this image.
        """
        dataset_dict = copy.deepcopy(dataset_dict)

        # Read and augment image
        image = utils.read_image(
            dataset_dict["file_name"], format=self.image_format
        )
        aug_input = T.AugInput(image)
        transforms = T.AugmentationList(self.augmentations)(aug_input)
        image = aug_input.image
        image_shape = image.shape[:2]

        dataset_dict["image"] = torch.as_tensor(
            np.ascontiguousarray(image.transpose(2, 0, 1))
        ).float()

        if not self.is_train:
            dataset_dict.pop("annotations", None)
            return dataset_dict

        # Process annotations
        annos = [
            utils.transform_instance_annotations(
                obj, transforms, image_shape
            )
            for obj in dataset_dict.pop("annotations", [])
            if obj.get("iscrowd", 0) == 0
        ]
        instances = utils.annotations_to_instances(annos, image_shape)
        if self.recompute_boxes:
            instances.gt_boxes = instances.gt_masks.get_bounding_boxes()
        dataset_dict["instances"] = utils.filter_empty_instances(instances)

        # Attach background pseudo-label
        file_name = dataset_dict.get("file_name", "")
        image_key = os.path.splitext(os.path.basename(file_name))[0]
        bg_label = self.bg_labels.get(image_key, 0)
        dataset_dict["bg_label"] = bg_label

        return dataset_dict
